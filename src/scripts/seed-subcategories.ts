async function seedSubCategories() {
  console.log("Connecting to MongoDB...");
  await mongoose.connect(env.MONGODB_URI);
  console.log("Connected.\n");

  // Delete all existing sub-categories
  const deleted = await SubCategory.deleteMany({});
  console.log(`Deleted ${deleted.deletedCount} existing sub-categories.\n`);

  let totalCreated = 0;

  for (const [parentSlug, items] of Object.entries(subCategoriesByParent)) {
    const parent = await Category.findOne({ slug: parentSlug });
    if (!parent) {
      console.warn(`  ⚠ Parent category "${parentSlug}" not found — skipping ${items.length} items`);
      continue;
    }

    console.log(`\n📁 ${parent.name} (${items.length} items)`);

    for (const item of items) {
      await SubCategory.create({
        name: item.name,
        slug: item.slug,
        format: item.format,
        description: item.description,
        category: parent._id,
        sortOrder: item.sortOrder,
      });
      console.log(`   ✓ ${item.sortOrder}. ${item.name} [${item.format}]`);
      totalCreated++;
    }

    // Update parent category asset count
    await Category.findByIdAndUpdate(parent._id, { assetCount: items.length });
  }

  console.log(`\n✅ Done! ${totalCreated} sub-categories seeded across ${Object.keys(subCategoriesByParent).length} categories.`);

  await mongoose.disconnect();
  process.exit(0);
}

seedSubCategories().catch((err) => {
  console.error("Seed sub-categories failed:", err);
  process.exit(1);
});
